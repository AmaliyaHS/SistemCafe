    private void insertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertButtonActionPerformed
        // TODO add your handling code here:
        String nama = namaField.getText();
        String id = idField.getText();
        try {
            openDb();
            s.executeUpdate(
            "INSERT INTO `karyawan` VALUE ('" + nama + 
                "','" + id + "')");
                            
            Object[] row = {nama, id};
            model.addRow(row);

            JOptionPane.showMessageDialog(null, "Data added!");

        } catch(SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, 
                ex.getLocalizedMessage());
        } finally {
            closeDb();
        }
    }//GEN-LAST:event_insertButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        int row = kasirTable.getSelectedRow();
        String nama = namaField.getText();
        String id = idField.getText();
        String selectNama = model.getValueAt(row, 0).toString();
        String selectId = model.getValueAt(row, 1).toString();
        
        if(kasirTable.getSelectedRowCount() == 1) {
            try {
                openDb();
                if(nama.equals("") && !id.equals("")) {
                    model.setValueAt(selectNama, row, 0);
                    model.setValueAt(id, row, 1);
                    ps = c.prepareStatement("UPDATE `karyawan` SET namaPegawai"
                    + " = '" + selectNama + "', idPegawai = '" + id + "' WHERE "
                    + "namaPegawai = '" + selectNama + "'");
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Edit Successfully");
                } else if(id.equals("") && !nama.equals("")) {
                    model.setValueAt(nama, row, 0);
                    model.setValueAt(selectId, row, 1);
                    ps = c.prepareStatement("UPDATE `karyawan` SET namaPegawai"
                    + " = '" + nama + "', idPegawai = '" + selectId + "' WHERE "
                    + "idPegawai = '" + selectId + "'");
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Edit Successfully");
                } else if(!nama.equals("") && !id.equals("")) {
                    model.setValueAt(nama, row, 0);
                    model.setValueAt(id, row, 1);
                    ps = c.prepareStatement("UPDATE `karyawan` SET namaPegawai"
                    + " = '" + nama + "', idPegawai = '" + id + "' WHERE "
                    + "idPegawai = '" + selectId + "'");
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Edit Successfully");
                } else if(nama.equals("") && nama.equals("")) {
                    JOptionPane.showMessageDialog(null, "Harap Mengisi Nama "
                        + "atau ID");
                
                }
            } catch(SQLException | ClassNotFoundException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
            } finally {
                closeDb();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Harap Edit Satu per Satu");
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = kasirTable.getSelectedRow();
        String selected = model.getValueAt(row, 0).toString();
        
        if(kasirTable.getSelectedRowCount() == 1) {
            model.removeRow(kasirTable.getSelectedRow());
            try {
                openDb();
                ps = c.prepareStatement("DELETE FROM `karyawan` WHERE "
                + "namaPegawai = '"+ selected + "'");
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Delete Successfully");
            } catch(SQLException | ClassNotFoundException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
            } finally {
                closeDb();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Harap Delete Satu per Satu");
        }                            
    }//GEN-LAST:event_deleteButtonActionPerformed

