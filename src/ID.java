    private static void setupTable() {
        model.addColumn("Nama");
        model.addColumn("ID");
        
        try {
            openDb();
            rs = s.executeQuery("SELECT * FROM `karyawan`");
            
            while(rs.next()) {
                
                Object[] row = {
                    rs.getString("namaPegawai"),
                    rs.getString("idPegawai")
                };
                model.addRow(row);
            }
        }catch(SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();
        } finally {
            closeDb();
        }
    }

