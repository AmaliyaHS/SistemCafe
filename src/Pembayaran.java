    private static void setupTable() {
        model.addColumn("Tanggal");
        model.addColumn("Jam");
        model.addColumn("No. Meja");
        model.addColumn("Nama");
        model.addColumn("Jumlah");
        model.addColumn("Harga");
        model.addColumn("Total");
        
        try {
            openDb();
            rs = s.executeQuery("SELECT * FROM `pemesanan`");
            
            while(rs.next()) {
                Object[] row = {
                    rs.getString("tanggal"),
                    rs.getString("jam"),
                    rs.getString("nomor"),
                    rs.getString("nama"),
                    rs.getString("jml"),
                    rs.getString("harga"),
                    rs.getString("total")
                };
                model.addRow(row);
            }
        }catch(SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();
        } finally {
            closeDb();
        }
    }
    
    private static void tampilTable() {
        dtm.addColumn("Nama");
        dtm.addColumn("Jumlah");
        dtm.addColumn("Harga");
        dtm.addColumn("Total");
        
        int row = model.getRowCount();
            
        for(int i = 0; i < row; i++) {
            Object[] tabel = { 
                model.getValueAt(i, 3), model.getValueAt(i, 4),
                model.getValueAt(i, 5), model.getValueAt(i, 6)
            };
            dtm.addRow(tabel);
        }
    }
    
    

