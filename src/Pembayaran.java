    public void currentTime() {
        Thread clock = new Thread() {
            public void run() {
                try {
                    for(;;) {
                    Calendar cal = new GregorianCalendar();
                    int month = cal.get(Calendar.MONTH);
                    int year = cal.get(Calendar.YEAR);
                    int day = cal.get(Calendar.DATE);
                    tanggalLabel.setText(year + "-" + (month+1) + "-" + day);

                    int second = cal.get(Calendar.SECOND);
                    int minute = cal.get(Calendar.MINUTE);
                    int hour = cal.get(Calendar.HOUR);
                    jamLabel.setText(hour + ":" + minute + ":" + second);
                    sleep(1000);
                    }
                }catch (InterruptedException e) {
                    e.printStackTrace();
                }
            }
        };
        clock.start();
    }
    
    private void filter(String query) {
        model = (DefaultTableModel) bayarTable.getModel();
        TableRowSorter<DefaultTableModel> tr = new 
        TableRowSorter<DefaultTableModel> (model);
        
        bayarTable.setRowSorter(tr);
        
        if(query != "No. Meja") {
            tr.setRowFilter(RowFilter.regexFilter(query, 2));
        } else {
            bayarTable.setRowSorter(tr);
        }
    }
    
    private void search(String key) {
        try {
            openDb();
            rs = s.executeQuery("SELECT `nama`, `jml`, `harga`, `total` FROM "
                + "`pemesanan` WHERE `nomor` = '" + key + "'");
            dtm.getDataVector().removeAllElements();
            
            while(rs.next()) {
                Object[] row = {
                    rs.getString("nama"),
                    rs.getString("jml"),
                    rs.getString("harga"),
                    rs.getString("total")
                };
                dtm.addRow(row);
            }
        }catch(SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();
        } finally {
            closeDb();
        }
    }
    
    private void done(String date, String namaKasir, int 
        pendapatan) {
        try {
            openDb();
            s.executeUpdate(
            "INSERT INTO `laporan` VALUE (" + transaksi + ", '" + date + "', '"
            + namaKasir + "', " + pendapatan + ")");
                            
            Object[] row = {transaksi, date, namaKasir, pendapatan};
            model.addRow(row);

            JOptionPane.showMessageDialog(null, "Data added!");
        } catch(SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
        } finally {
            closeDb();
        }  
    }

