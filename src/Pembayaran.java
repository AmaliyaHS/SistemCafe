    private void hitungButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungButtonActionPerformed
        // TODO add your handling code here:
        int jumlah = 0;
        
        for(int i = 0; i < bayarTable.getRowCount(); i++) {
            String angka = bayarTable.getValueAt(i, 6).toString();
            jumlah += Integer.parseInt(angka);
        }
        
        int diskon = (int) diskonSpinner.getValue();
        int totalDiskon = jumlah * diskon / 100;
        int total = jumlah - totalDiskon;
        
        String tulisan = bayarField.getText();
        int bayar = Integer.parseInt(tulisan);
        
        int kembalian = bayar - total;
        if(kembalian >= 0) {
            String ubah3 = String.valueOf(kembalian);
            kembalianField.setText(ubah3);
        } else {
            JOptionPane.showMessageDialog(null, "Jumlah Uangnya Kurang");
        }
    }//GEN-LAST:event_hitungButtonActionPerformed

